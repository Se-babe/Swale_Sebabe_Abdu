#types of machine learning:
#1. Supervised learning: Involves training a model on labeled data, where the input data is paired with the correct output. The model learns to map inputs to outputs and can make predictions on new, unseen data.

'''common algorithms in supervised learning include:
- Linear Regression
- Logistic Regression
- Decision Trees
- Support Vector Machines (SVM)
- Neural Networks
- k-Nearest Neighbors (k-NN)
- Random Forests

-Deep learning: image classification A subset of machine learning that uses neural networks with many layers (deep neural networks) to model complex patterns in large datasets. It is particularly effective for tasks like image and speech recognition.

'''
#2. Unsupervised learning: Involves training a model on unlabeled data, where the model tries to learn the underlying patterns and structures in the data without explicit guidance on what the outputs should be.
'''  -k-means clustering: A method that partitions data into k distinct clusters based on feature similarity.
     - Hierarchical clustering: A method that builds a hierarchy of clusters by either merging smaller clusters into larger ones or splitting larger clusters into smaller ones.
     - Principal Component Analysis (PCA): A dimensionality reduction technique that transforms data into a lower-dimensional space while preserving as much variance as possible.
     - Association rule learning: A method that discovers interesting relationships between variables in large datasets, often used in market basket analysis.'''
