#variables# Variables are used to store data in Python. You can create a variable by assigning a value to
# it using the equals sign (=). Variable names can contain letters, numbers, and underscores, but they cannot start with a number.
# Example:
name = "John"  # String variable
age = 30  # Integer variable
height = 5.9  # Float variable
# You can print the values of variables using the print() function.
print(name)  # Output: John
print(age)   # Output: 30
print(height)  # Output: 5.9
#data types
# Python has several built-in data types, including:
# 1. Integers: Whole numbers, e.g., 10, -5, 0
# 2. Floats: Decimal numbers, e.g., 3.14, -
2.5, 0.0
# 3. Strings: Text enclosed in quotes, e.g., "Hello", 'Python
# 4. Booleans: True or False values, e.g., True, False
# 5. Lists: Ordered collections of items, e.g., [1, 2, 3], ["apple", "banana"]
# 6. Tuples: Immutable ordered collections, e.g., (1, 2, 3), ("apple", "banana")
# 7. Dictionaries: Key-value pairs, e.g., {"name": "John", "age": 30}
# 8. Sets: Unordered collections of unique items, e.g., {1, 2, 3}, {"apple", "banana"}
# You can check the type of a variable using the type() function.
print(type(name))  # Output: <class 'str'>
print(type(age))   # Output: <class 'int'>
print(type(height))  # Output: <class 'float'>
# You can also convert between data types using functions like int(), float(), str(), etc.
# Example:
age_str = str(age)  # Convert integer to string

print(age_str)  # Output: "30"
print(type(age_str))  # Output: <class 'str'>
